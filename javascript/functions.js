// アロー関数は、簡略化された関数の書き方です。無名関数を使うときに便利です。
// const greet = (name) => "Hello, " + name + "!";
// console.log(greet("Charlie")); 
// =>：アロー関数の記号です。左辺に引数、右辺に処理を書きます。
// {}：処理が1行の場合は省略できます。
// return：処理が1行の場合は省略できます。
// ()：引数が1つの場合は省略できます。


